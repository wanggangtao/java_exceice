package demo02;

public class case_swich {
    public static void main(String args[]){
        int i = 1;
        switch(i){
            case 0:
                System.out.println("0");
                break;
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("default");
        }
    }
}